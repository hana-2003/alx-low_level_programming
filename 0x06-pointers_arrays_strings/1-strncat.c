/**
*_strncat-concatenatetwostrings
*usingatmostnbytesfromsrc
*@dest:inputvalue
*@src:inputvalue
*@n:inputvalue
*Return:dest
*/
char*_strncat(char*dest,char*src,intn)
{
inti;
intj;
i=0;
while(dest[i]!='\0')
{
i++;
}
j=0;
while(j<n&&src[j]!='\0')
{
dest[i]=src[j];
i++;
j++;
}
dest[i]='\0';
return(dest);
}
